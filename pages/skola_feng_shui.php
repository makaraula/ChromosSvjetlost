	      <p>Feng Shui je drevna kineska metoda planiranja i uređenja prostora kako bi se u dom unijela ravnoteža i blagostanje. Smatra se da se Feng Shui umijeće počeo koristiti prije najmanje 4000 godina, iako neki vjeruju da je star i preko šest tisutljeća. Najraniji arheološki dokazi datiraju iz razdoblja od prije 5000 godina. Tijekom vremena, izvorni naziv Kan Yu (kin. studija neba i zemlje) zamijenjen je s nazivom Feng Shui (kin. vjetar i voda).</p>
	      <p>U početku je Feng Shui služio za uređenje grobova i grobnica kako bi se nasljednicima preminulog osigurala sreća kroz generacije. Kasnije, nadogradnjom postojećih formula i dijagrama, počeo se koristiti za uređenje domova živih ljudi.</p>
	      <p>Glavni cilj Feng Shuija je stvoriti skladan prostor koji će pozitivno djelovati na zdravlje, život i rad ukućana.</p>
	      <p>Feng shui povezan je s drevnom kineskom filozofijom prema kojoj je cijeli svemir prožet energijom chi, a ona se manifestira kroz pozitivnu yang i negativnu yin energiju, između kojih je potrebno stvoriti ravnotežu. Yin, energija zemlje, ženski princip, složen je i taman, a yang, energija neba, muški princip, svijetli je i jednostavan. Ta dva pola su u prirodi su u dinamičkoj ravnoteži.</p>
	      <p>Osim rasporeda namještaja, boje su jedno od najvažnijih pomagala, kojima stručnjaci za Feng Shui usmjeravaju chi i blagodatno mijenjaju energiju određenih područja u domu. Kada su odgovarajuće, boje podstiču prosperitet, ljubav i zdravlje, dok upotrebljene na pogrešan način mogu imati suprotan efekat. Zidovi u boji u trenu mogu osvježiti prostor, no glavni problem je koje boje odabrati i kako ih kombinirati. Ako niste sigurni što bi odgovaralo uz vaš namještaj ili se dvoumite između nekoliko različitih nijansi, pročitajte savjete drevne Feng shui vještine.</p>
	      <p>Feng Shui vještina temelji se na 5 elemenata – vatra, metal, zemlja, drvo, voda. Svaki element ima svoje boje koje imaju različiti utjecaj na ljude.</p>

