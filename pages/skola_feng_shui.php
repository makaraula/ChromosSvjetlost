	          <p><strong>Voda = tamno plava, crna</strong></p>
	          <p>Voda je izvor mudrosti, inteligencije, mira i svježine, a njene boje su plava i crna. Plava korištena na zidovima ili predmetima na sjeveru donosi napredak u karijeri, na istoku zdravlje, a na jugoistoku novac. Zbog svojih osobina, crna unosi dubinu, snagu i definiciju u svaki prostor. Ipak, naravno ne bi trebalo da se nalazi u dječjoj sobi ili na stropu spavaće sobe.</p>
