	          <p><strong>Kuhinja</strong></p>
	          <p>Blagovaonica i kuhinja neka budu tople, ali svjetlije nijanse, npr., boje lososa ili breskve. One će umjereno potaknuti apetit. Uz element metal vežu se bijela, bež i srebrna. To su sve boje koje su prikladne za kuhinju jer asociraju na čistoću koja je nužna u pripremi hrane. Osim toga metal je kao element kompatibilan s vatrenim elementom koji dominira kuhinjom. Preporuča se da je pod u tonovima boje zemlje, a zidovi u toplim pastelnim tonovima boje vanilije ili breskve.</p>
