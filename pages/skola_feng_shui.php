	          <p><strong>Vatra = crvena, narančasta</strong></p>
	          <p>Vatra predstavlja strastvenost, energičnost i inicijativu, a njene boje su crvena, narandžasta i roze. Sever, severoistok i jugozapad stana ili kuće prava su mesta za te boje. Crvena najbolje oslikava osobine vatre kao elementa feng šuija.  Za jarku crvenu boju smatra se da u dom unosi energiju radosti i uzbuđenja i da razbuktava seksualnu želju. Stoga može biti idealan izbor za spavaću sobu. Ukoliko vam je ipak crvena boja mnogo jaka, kao alternativa može poslužiti i narandžasta. Ta boja važi za društvenu boju, jer donosi energiju za zanimljive razgovore, budi optimizam i otvorenost. Stručnjaci za feng šuiji savetuju umereno korišćenje ljubičaste. Oni smatraju da je bolje rešenje ljubičastu uneti u dom  preko različitih predmeta nego njome okrečiti zidove. Roze je univerzalna boja ljubavi, pa je odličan izbor ako želite da smekšate energiju u prostoru. Smatra se da deluje umirujuće na ponašanje, a može se koristiti u svim prostorijama.</p>
	          <p><strong>Zemlja = žuta</strong></p>
