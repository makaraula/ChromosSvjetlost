	          <p>Žuta kao boja elementa zemlje potiče komunikaciju i otvorenost prema drugim ljudima pa su razne tople varijante žute pogodne za uređenje dnevnog boravka.</p>
	          <p>Crvena je boja vatrenog elementa. Ona otvara apetit i potiče metabolizam, ali s druge strane, može poticajno djelovati i na ljudsku agresivnost. Ako ste ljubitelj ove boje, radije odaberite svjetlije nijanse crvene ili je svedite na detalje u dnevnoj sobi i kuhinji koji će unijeti živosti i topline, ali neće djelovati preagresivno.</p>
	          <p><strong>Spavaća soba</strong></p>
	          <p>Uz element vode vežu se tamnoplava i crna. Plava smiruje, ali i potiče na razmišljanje o životu i svijetu. Svjetliji tonovi plave pogodni su za uređenje spavaće sobe. Za spavaonicu možete odabrati jednako smirujuće, ali lakše tonove u boji lavande ili masline.</p>
	          <p>Crvenu, osobito zgusnutu, jarku treba izbjegavati u spavaćoj sobi jer može ometajuće djelovati na san, ali pojedini detalji poput crvenih jastučića mogu probuditi strast u pravom trenutku.</p>
	          <p>Ljubičasta djeluje izrazito smirujuće na čovjekovu psihu pa se često koristi u prostorima za meditaciju, a prikladna je i za spavaće sobe.</p>
	          <p><strong>Kupaonica</strong></p>
	          <p>Bijela pročišćava prostor, a kako ne bi izgledala presterilno i dosadno, dodajte malo toplijih nijansi.</p>
